def get_user_input():
    """
    Prompt the user to enter a path.

    Returns:
    - str: The user-entered path.
    """
    return input("Please enter the path name: ")

def get_user_input_url():
    """
    Prompt the user to enter a repository url.

    Returns:
    - str: The user-entered repository url.
    """
    return input("Please enter the repository url name: ")


